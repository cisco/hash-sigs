/*
 * This will test out the underlying SHAKE256 primitive
 *
 * We do this to verify whether the SHAKE256 implementation we include in
 * this package is actually correct
 */

#include <stdbool.h>
#include <string.h>
#include "hash.h"
#include "test_hss.h"

/*
 * Run the test vector on the various internal SHAKE APIs
 */
static bool do_hash_test( const unsigned char *input, unsigned len_input,
                          const unsigned char *expected_output,
                          int hash_type, unsigned len_output ) {
    unsigned char output[32+1];

    int fence;
    for (fence=3; fence<=4; fence++) {
        output[len_output] = fence;   /* To detect it if the hash function */
            /* generated a longer output than it is supposed to */

        /* Check the simple hash implementation */
        memset( output, 0, len_output );
        hss_hash(output, hash_type, input, len_input );
            /* Check if the output is what we expect */
        if (0 != memcmp( output, expected_output, len_output )) return false;
            /* Check if the hash function generated more output than */
            /* we expect */
        if (output[len_output] != fence) return false;

        /* Check the context implementation */
        union hash_context ctx;
        memset( output, 0, len_output );
        hss_hash_ctx(output, hash_type, &ctx, input, len_input );
        if (0 != memcmp( output, expected_output, len_output )) return false;
        if (output[len_output] != fence) return false;

        /* Check the incremental version, stepping through the possible */
        /* increments */
        int incr;
        for (incr = 1; incr <= len_input; incr++) {

            hss_init_hash_context( hash_type, &ctx );
            int offset;
            for (offset=0; offset < len_input; offset += incr ) {
                int this_block = incr;
                if (offset + this_block > len_input) {
                    this_block = len_input - offset;
                }
                hss_update_hash_context( hash_type, &ctx,
                          &input[offset], this_block );
            }
            memset( output, 0, len_output );
            hss_finalize_hash_context( hash_type, &ctx, output );
            if (0 != memcmp( output, expected_output, len_output )) {
                return false;
            }
            if (output[len_output] != fence) return false;
        }
    }
    return true;
}

/*
 * Run the test vector on both SHAKE hash functions (the 256 bit and the 192
 * bit versions) that we provide
 */
static bool do_test( const unsigned char *input, unsigned len_input,
                     const unsigned char *expected_output ) {
    if (!do_hash_test( input, len_input, expected_output, HASH_SHAKE256, 32 ))
        return false;
    if (!do_hash_test( input, len_input, expected_output, HASH_SHAKE256_24, 24 ))
        return false;
    return true;
}

